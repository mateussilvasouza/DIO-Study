/**TDD (Test Driven Development)
 * É um dos pilares do Extreme programming, consiste em testar e
 * refatorar em pequenos ciclos, onde você escreve o teste antes
 * do código, faz o mesmo passar e refatora o código.
 * 
 * Escrita do teste -> escrita do código -> refatoração
 *
 **Vantagens:
 * Feedback rápido;
 * Maior segurança em alterações e novas funcionalidades;
 * Código mais limpo;
 * Produtividade.
 */

/**BDD (Behavior Driven Development)
 * 
 * Técnica de Desenvolvimento Ágil que visa integrar regras de
 * negócio com linguagens de programação.
 * 
 * *Pilares:
 * Testes;
 * Documentação;
 * Exemplos.
 * 
 * *Vantagens:
 * Compartilhamento de conhecimento;
 * Documentação Dinâmica;
 * Visão do todo.
 */